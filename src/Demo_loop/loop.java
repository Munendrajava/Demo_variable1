package Demo_loop;

public class loop {
    public static void main(String[] args) {
        int a = 8;
        //use for loop
        for (int i = 0; i < 11; i++) {
            //print table of 8
            System.out.println("table of eight:::" + a + "*" + i + "=" + 8 * i);
        }
    }
}




